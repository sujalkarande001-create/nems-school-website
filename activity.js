const params = new URLSearchParams(window.location.search);
const key = params.get("activity") || "classroom-learning";
const activity = activities[key] || activities["classroom-learning"];

document.title = `${activity.title} | New English Medium School`;
document.querySelector("#activity-kicker").textContent = activity.kicker;
document.querySelector("#activity-title").textContent = activity.title;
document.querySelector("#activity-description").textContent = activity.description;

const slides = [["Main Moment", activity.hero], ...activity.photos];
const slideImage = document.querySelector("#activity-slide-image");
const slideCaption = document.querySelector("#activity-slide-caption");
const prevButton = document.querySelector("#activity-prev");
const nextButton = document.querySelector("#activity-next");
let slideIndex = 0;
let slideTimer;

function showSlide(index) {
  slideIndex = (index + slides.length) % slides.length;
  const [caption, src] = slides[slideIndex];
  slideImage.src = src;
  slideImage.alt = `${activity.title} - ${caption}`;
  slideCaption.textContent = caption;
}

function startSlider() {
  clearInterval(slideTimer);
  slideTimer = setInterval(() => showSlide(slideIndex + 1), 3500);
}

prevButton.addEventListener("click", () => {
  showSlide(slideIndex - 1);
  startSlider();
});

nextButton.addEventListener("click", () => {
  showSlide(slideIndex + 1);
  startSlider();
});

showSlide(0);
startSlider();
